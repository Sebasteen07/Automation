		/**
		 * Verifies autogenerated username uniqueness through credentialMatcher, max 10 attempts
		 *
		 * @param testData
		 * @throws NullPointerException
		 * @throws Exception
		 */
		public JalapenoPatient(PropertyFileLoader testData) throws NullPointerException, Exception {
				IHGUtil.PrintMethodName();

				username = PortalUtil2.generateUniqueUsername(testData.getUserId(),testData);
				email = username + "@mailinator.com";
				firstName = testData.getFirstName() + IHGUtil.createRandomNumber();
				lastName = testData.getLastName();
				password = testData.getPassword();
				gender = Patient.GenderExtended.MALE;
				url = testData.getUrl();
				DOBDay = testData.getDOBDay();
				DOBMonth = testData.getDOBMonth();
				DOBMonthText = testData.getDOBMonthText();
				DOBYear = testData.getDOBYear();
				ZipCode = testData.getZipCode();
				// TODO fix this hack - sak nemame pristup do com.medfusion.common.utils, tak co? :)
				address1 = "U rychtare gejoty 8";
				address2 = "U bobra 12";
				city = "Mamlasov";
				state = "Alabama";
		}

		public String getEmail() {
				return email;
		}

